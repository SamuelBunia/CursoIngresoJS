/*
al presionar el botón mostrar 10 repeticiones 
con números DESCENDENTES, desde el 10 al 1.*/
function mostrar()
{
	let numero=10; //Valor inicial de la variable de control

	//tiene condicion y bloque de codigo
	while (numero > 0) { //condicion de la repititiva *si el numero es menor a 10*
		console.log(numero); //mostrar mi numero
		numero--; //Que vaya aumentando en 1 // modificacion de la variable de control

	}


}//FIN DE LA FUNCIÓN