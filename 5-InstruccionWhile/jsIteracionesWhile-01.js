/*
al presionar el botón mostrar 10 repeticiones 
con números ASCENDENTE, desde el 1 al 10.*/
function mostrar()
{
	let numero=0; //Valor inicial de la variable de control

	//tiene condicion y bloque de codigo
	while (numero < 10) { //condicion de la repititiva *si el numero es menor a 10*
		numero++; //Que vaya aumentando en 1 // modificacion de la variable de control
		console.log(numero); //mostrar mi numero

	}


}//FIN DE LA FUNCIÓN